<h2>Distributed Estimation</h2>
<p align="justify">In this project, we are developing methods for
	estimation and control in distributed camera networks. Specifically, we
	have looked at the following problems.</p>
<div class="list">
	<ul>
		<li>Distributed estimation algorithms that are aware of the specific
			constraints of camera networks</li>
		<li>Distributed control for collaborative and opportunistic sensing in
			wide-area camera networks</li>
	</ul>
</div>
