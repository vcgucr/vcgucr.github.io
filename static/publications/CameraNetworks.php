<div id='Active Sensing' class="spacer"></div>
<h2>Active Sensing</h2>
<p align="justify">We are developing methods for optimizing the image acquisition capabilities of the cameras so 
as to maximize the performance of the methods used to analyze these image. This needs to be done through collaboration 
between the cameras in a network, as each camera's parameters entail constraints on the others. We have developed 
distributed control methods for collaborative and opportunistic sensing in wide-area camera networks.</p>
