<h2>Camera Network Tracking and Re-identification</h2>
<p align="justify">In many computer vision tasks it is often desirable
	to identify and monitor people as they move through a network of
	non-overlapping cameras. This is especially challenging as for a
	network of cameras issues such as changes of scale, illumination,
	viewing angle and pose start to arise. In this project we try to
	address this inter camera person association problem. We have shown
	that incorporating a consistency requirement of re-identification
	results across the camera network can significantly improve the
	re-identification performance even in the challenging scenarios where
	the illumination changes between the cameras are large.</p>
